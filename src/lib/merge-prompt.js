export function buildMergeUserPrompt(documents) {
  const docSections = documents
    .map((doc, i) => `--- 版本 ${i + 1}: ${doc.name} ---\n${doc.text}`)
    .join('\n\n')

  return `请将以下 ${documents.length} 个版本的需求文档整合为一份完整的规范化 PRD。

文档按版本从旧到新排列，后面的版本优先级更高：

${docSections}

请按照模板结构输出完整的 Markdown 格式 PRD。`
}
