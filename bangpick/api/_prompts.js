// Stock-analyze system prompts. Compressed for Flash-tier models — long
// system prompts disproportionately slow GLM-4.5-Flash / Gemini Flash TTFT
// and degrade instruction following. Each intent stays small and example-led;
// the example IS the spec, redundant rule lists below it have been removed.
//
// Hard constraints preserved across the rewrite:
// - All verdict literals the frontend parses with regex (StockChat.jsx)
// - Mode A/B distinction in holding (with-cost vs without-cost)
// - 100-point scoring thresholds (≥90 / 70-89 / <70)
// - Position size caps (single 15%, total 30%; holding ≥90 → 20-30%, 70-89 → 10-15%)
// - "暂不建议买入" must NOT include buy range
// - 跌破止损位 → 强制清仓 / 必须立即卖出离场 wording

